import 'package:flutter_screenutil/flutter_screenutil.dart';

class AppSize {
  static double height({required double height}) {
    return height.h;
  }

  static double width({required double width}) {
    return width.w;
  }

  static double radius({required double radius}) {
    return radius.r;
  }

  static double fontSize({required double fontSize}) {
    return fontSize.sp;
  }
}
